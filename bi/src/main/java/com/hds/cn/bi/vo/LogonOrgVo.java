package com.hds.cn.bi.vo;

public class LogonOrgVo {
	private String date;
	private int publishActivityCnt;
	private int orderCnt;
	private int orderAmount;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getPublishActivityCnt() {
		return publishActivityCnt;
	}
	public void setPublishActivityCnt(int publishActivityCnt) {
		this.publishActivityCnt = publishActivityCnt;
	}
	public int getOrderCnt() {
		return orderCnt;
	}
	public void setOrderCnt(int orderCnt) {
		this.orderCnt = orderCnt;
	}
	public int getOrderAmount() {
		return orderAmount;
	}
	public void setOrderAmount(int orderAmount) {
		this.orderAmount = orderAmount;
	}
}
