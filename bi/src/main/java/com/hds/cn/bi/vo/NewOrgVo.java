public class NewOrgVo {
	private String date;
	private int registOrgCnt;
	private int publishActivityCnt;
	private int tradeOrgCnt;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getRegistOrgCnt() {
		return registOrgCnt;
	}
	public void setRegistOrgCnt(int registOrgCnt) {
		this.registOrgCnt = registOrgCnt;
	}
	public int getPublishActivityCnt() {
		return publishActivityCnt;
	}
	public void setPublishActivityCnt(int publishActivityCnt) {
		this.publishActivityCnt = publishActivityCnt;
	}
	public int getTradeOrgCnt() {
		return tradeOrgCnt;
	}
	public void setTradeOrgCnt(int tradeOrgCnt) {
		this.tradeOrgCnt = tradeOrgCnt;
	}
}
