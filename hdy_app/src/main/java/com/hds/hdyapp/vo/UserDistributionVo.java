package com.hds.hdyapp.vo;

public class UserDistributionVo {
	private int regionId;
	private int orderUser;
	private String orderUserRate;
	
	public int getRegionId() {
		return regionId;
	}
	public void setRegionId(int regionId) {
		this.regionId = regionId;
	}
	public int getOrderUser() {
		return orderUser;
	}
	public void setOrderUser(int orderUser) {
		this.orderUser = orderUser;
	}
	public String getOrderUserRate() {
		return orderUserRate;
	}
	public void setOrderUserRate(String orderUserRate) {
		this.orderUserRate = orderUserRate;
	}
}
